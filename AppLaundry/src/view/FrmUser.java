    private void refreshTable(){
        if(proses.equalsIgnoreCase("")){
           listPelanggan = control.selectPelanggan("");
        }

        dtm = (DefaultTableModel) tblPelanggan.getModel();
        dtm.setRowCount(0);
        for (PelangganModel data : listPelanggan){
            dtm.addRow(new Object[]{
                        data.getNama(),
                        data.getTelp(),
                        data.getGender(),
                        data.getAlamat()});
        }

        if(tblPelanggan.getRowCount() > 0){
            row = tblPelanggan.getRowCount() - 1;
            tblPelanggan.setRowSelectionInterval(row, row);
//            aturButton(true);
//            aturText(true);
            proses = "";
        }
    }
