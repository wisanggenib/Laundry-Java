    private void btnTambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTambahMouseClicked
        // TODO add your handling code here:
        String gender;
        if(rdbLaki.isSelected()){
            gender = "laki";
        }else if(rdbPerempuan.isSelected()){
            gender = "Perempuan";
        }else{
            gender = "";
        }
        
        String kosong = "";
        
        if(txtNama.getText().equals("") || 
                txtTelp.getText().equals("") ||
                txtAlamat.getText().equals("") || 
                gender.equals("")){
            JOptionPane.showMessageDialog(null, "Isi Data Terlebih Dahulu");
        }else{
        control.inputPelanggan(txtNama.getText(), txtTelp.getText(),
                           gender, txtAlamat.getText());
        //insert ke tabel staff
        boolean hasil = control.insertPelanggan();
        if(hasil == false){
            JOptionPane.showMessageDialog(null, "Penyimpanan gagal");
        }
        
        txtNama.setText("");
        txtTelp.setText("");
        txtAlamat.setText("");
        rdbLaki.setSelected(false);
        rdbPerempuan.setSelected(false);
        refreshTable();
        }
    }//GEN-LAST:event_btnTambahMouseClicked

